package com.model;

public class Manager {
	private int man_id;
	private String man_loginname;
	private String man_name;
	private String man_pwd;
	private String man_range;

	public int getMan_id() {
		return man_id;
	}

	public void setMan_id(int man_id) {
		this.man_id = man_id;
	}

	public String getMan_loginname() {
		return man_loginname;
	}

	public void setMan_loginname(String man_loginname) {
		this.man_loginname = man_loginname;
	}

	public String getMan_name() {
		return man_name;
	}

	public void setMan_name(String man_name) {
		this.man_name = man_name;
	}

	public String getMan_pwd() {
		return man_pwd;
	}

	public void setMan_pwd(String man_pwd) {
		this.man_pwd = man_pwd;
	}

	public String getMan_range() {
		return man_range;
	}

	public void setMan_range(String man_range) {
		this.man_range = man_range;
	}

}
