package com.model;

public class SmallClass {
	private int smallclass_id;
	private String small_name;
	private int bigclass_id;

	
	public int getSmallclass_id() {
		return smallclass_id;
	}

	public void setSmallclass_id(int smallclass_id) {
		this.smallclass_id = smallclass_id;
	}

	public String getSmall_name() {
		return small_name;
	}

	public void setSmall_name(String small_name) {
		this.small_name = small_name;
	}

	public int getBigclass_id() {
		return bigclass_id;
	}

	public void setBigclass_id(int bigclass_id) {
		this.bigclass_id = bigclass_id;
	}

}
